// Ensure provider defined types fully satisfy framework interfaces
var _ resource.Resource = &MobyDickResource{}
var _ resource.ResourceWithImportState = &MobyDickResource{}

func NewMobyDickResource() resource.Resource {
	return &MobyDickResource{}
}

// MobyDickResource defines the resource implementation.
type MobyDickResource struct {
	client *http.Client
}

// MobyDickResourceModel describes the resource data model.
type MobyDickResourceModel struct {
	Id   types.String `tfsdk:"id"`
	Text types.String `tfsdk:"text"`
}

func (r *MobyDickResource) Metadata(ctx context.Context, req resource.MetadataRequest, resp *resource.MetadataResponse) {
	resp.TypeName = req.ProviderTypeName + "_moby_dick"
}

func (r *MobyDickResource) Schema(ctx context.Context, req resource.SchemaRequest, resp *resource.SchemaResponse) {
	resp.Schema = schema.Schema{
		// This description is used by the documentation generator and the language server.
		MarkdownDescription: "Moby Dick in state!",

		Attributes: map[string]schema.Attribute{
			"id": schema.StringAttribute{
				Computed:            true,
				MarkdownDescription: "Example identifier",
				PlanModifiers: []planmodifier.String{
					stringplanmodifier.UseStateForUnknown(),
				},
			},
			"text": schema.StringAttribute{
				MarkdownDescription: "All of it",
				Computed:            true,
			},
		},
	}
}

func (r *MobyDickResource) Configure(ctx context.Context, req resource.ConfigureRequest, resp *resource.ConfigureResponse) {
	// Prevent panic if the provider has not been configured.
	if req.ProviderData == nil {
		return
	}

	client, ok := req.ProviderData.(*http.Client)

	if !ok {
		resp.Diagnostics.AddError(
			"Unexpected Resource Configure Type",
			fmt.Sprintf("Expected *http.Client, got: %T. Please report this issue to the provider developers.", req.ProviderData),
		)

		return
	}

	r.client = client
}

